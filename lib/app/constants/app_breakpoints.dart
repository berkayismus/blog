/// Ekran boyutları için statik breakpoint değerleri.
/// Bu değerler, duyarlı tasarımlarda (responsive design) kullanılır.
class AppBreakpoints {
  // Genellikle dikey tutulan telefonlar için.
  static const double mobile = 768;

  // Tabletler ve küçük dizüstü bilgisayarlar için.
  static const double tablet = 1200;

  // Büyük ekranlı dizüstü bilgisayarlar ve masaüstü bilgisayarlar için.
  static const double desktop = double.infinity;
}
